n = int(input("Який елемент послідовності хочете отримати: "))
counter = 1  # змінне значення k
a = 1  # Нульовий елемент послідовнсті
 # Змінна для підрахунку членів послідовності
if n >= 0:
    for counter in range(1, n + 1):
        a = (counter * a + 1) / counter  # Обчислюємо значення n-го елемента
        counter += 1

    print("%s елемент послідовності дорівнює: %s" % (counter-1, a))
else:
    print("Помилка")
